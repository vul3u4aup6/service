		<p style="font-size:20px"><strong>基礎講座。相關規定</strong></p><div class="clr"></div><p/>
 <hr>
		<p style="font-size:14px;text-align:right"><strong>102.09.04服務學習第一次授課教師月座談會議修訂通過</strong></p><div class="clr"></div><p/>
 
          　<div class="text">
            <p>一、講座時數：依內政部志願服務法規定，進行12小時6議題「服務學習基礎講座」。</p>
			<br/>
			<p>二、講座場次：</p>
			<p>&nbsp;&nbsp; &nbsp;　(一)本學期安排6場專題講座，學生任選4場參加。內容包括志願服務的內涵、志願服務倫理、自我瞭解及自我肯定、志願服務經驗分享、志願服務法規之認識及志願服務發展趨勢等課程。
			
            </p>
			<p>&nbsp;&nbsp; &nbsp;　(二)有關「志願服務經驗分享」與「志願服務倫理」二主題，由老師運用授課資料光碟資料，或邀請學長姐於課堂上與同學們分享與討論志工或人群服務經驗。</p>
			<br/>
			<p>三、講座認證：</p>
			<p>&nbsp;&nbsp; &nbsp;　(一)入場認證：參與講座同學需攜帶「學生證」及「服務學習證」，進行刷卡及蓋章認證。</p>
			<p>&nbsp;&nbsp; &nbsp;　(二)入場時限：<strong>17:30</strong>開始刷卡入場；<strong>18:10</strong>之後入場者，中心將蓋遲到章登記；後禁止入場。</p>
			<p>&nbsp;&nbsp; &nbsp;　(三)入場後離場：講座進行時，需出入場者請由後門進出，並填寫「出入場登記表」；登記離場者請於10分鐘內返回，違者將不予以認證。</p>
			<p>&nbsp;&nbsp; &nbsp;　(四)離場認證及補蓋章認證：<p>&nbsp;&nbsp; &nbsp;　1.請攜帶「服務學習證」，於講座結束後由工作人員蓋章認證。</p><p>&nbsp;&nbsp; &nbsp;　2.未帶「服務學習證」者，請於講座結束離場時，向現場工作人員索取「服務學習基礎講座證明單」，並填寫「補蓋章登記表」。</p><p>&nbsp;&nbsp; &nbsp;　3.請於該場次講座結束後<strong>兩週</strong>內，至服務學習發展中心補蓋認證章。「服務學習基礎講座證明單」範例。（詳如學生手冊附件6， P.174）</p></p>
			<br/>
			<p>四、服務學習證補發：</p>
			<p>&nbsp;&nbsp; &nbsp;　(一)「服務學習證」遺失者，請至服務學習發展中心補領，每人限補領乙次。</p>
			<p>&nbsp;&nbsp; &nbsp;　(二)中心將依據各場次學生證刷卡記錄及補聽講座記錄予以補蓋認證章。</p>
			<p>五、講座補聽程序：</p>
			<p>&nbsp;&nbsp; &nbsp;　(一)班級補聽：<p>&nbsp;&nbsp; &nbsp;　1.若講座時間與上課時間衝堂，使得全班同學皆無法參與此場次講座，班代或服學幹部可與授課老師討論，徵詢是否可「利用服務學習上課時間，排定全班一起補看講座轉錄影片」。</p><p>&nbsp;&nbsp; &nbsp;　2.若授課老師同意，則由授課老師向服務學習發展中心提出講座補聽申請，並請服務學習幹部至中心借閱該場次轉錄影片。</p><p>&nbsp;&nbsp; &nbsp;　3.影片觀看完畢後，由服務學習幹部收齊當天參與同學之服務學習認證卡，統一至中心補蓋認證章，並歸還講座轉錄影片，即完成該場次講座認證。</p><p>&nbsp;&nbsp; &nbsp;　<strong>※班級補聽程序是否須撰寫講座心得，由各班授課老師自行規範。</strong></p></p>
			<br/>
			<p>六、凡修畢通過本課程者，視同完成「志願服務基礎訓練課程」，得參加校內辦理之「志願服務特殊訓練課程」，完成後，得申請「志願服務記錄冊」。</p>
