          <p style="font-size:16px;text-align:center"><font size="4"><strong>服務學習就像一粒種子</strong></font></p>
		  <p style="font-size:14px;text-align:right"><font size="4"><strong>師資培育中心專任教授  胡憶蓓</strong></font></p>
		  <p>&emsp;&emsp;九十三年的五、六月，得知學校要推動大一新生全面推動社區服務十八小時的課程，我很「勇敢地」接下了主要負責人的職務。我期待和二十幾位老師一同接受前所未有的挑戰。尤其是我，除了授課之外，還要負責協助教師的教學工作。藉著教育訓練的專長，我編撰出這門課程的教師手冊，詳列教學內容與流程，藉此協助所有的授課老師。每位老師拿到教學指引資料無不讚賞一番。然而，我的第一學期教學過程，卻可用「慘不忍賭」四個字來形容。 
		  </p>　　 
		  <p>&emsp;&emsp;       許多學生上課姍姍來遲，擔心他們遺漏重要訊息，我不斷地重複重要的事務報告；上課不聽不打緊，竟然有人拿起手機開始玩起合照；還有小情侶手牽手地含情脈脈相看兩不厭，無視老師在台上口沫橫飛。雖然個性溫吞，不忍教訓學生，但自認秩序容忍度極高的我，竟然也常常停下來要求同學不要過分干擾我的教學過程。我很努力地講課與帶活動，卻總覺得像一頭無力的老牛拖著沉重的牛車，氣喘吁吁，舉步維艱。每當下課聲響，學生一哄而散，只留下我一人，靜靜地收拾滿桌子的教材資料與破碎無力的心情。收拾的動作很慢，慢到竟然可以拖30分鐘。不是細心、也不是謹慎，而是虛脫！剛才結束的兩節課，耗盡所有的力氣，我必須休息一下，才能步出教室做別的事情。偶爾有同學會留下來與我聊天、安慰我、為我打氣；也有同學建議我要兇一點，像他們的高中老師。
		  </p>
		  <p>&emsp;&emsp;教書多年來，這是我第一次感到嚴重挫敗的課程。每天「朝思暮想」，反省自己。透過這一門課，我想要學生有何改變？我期望學生在一學期中有多大的成長？這樣的期望合理嗎？若不，那可以期待什麼呢？如果沒有期待，課程又有何意義呢？我開始放慢腳步，細細地觀察身邊學生。我注意到不少學生每次都專心聽講；也看到了大男生靦腆卻認真的一面；還有人看似輕浮，但卻也負責地完成工作；當我讚美與鼓勵時，學生表現信心倍增的模樣等等。從學生的心得反省中，我細細品嘗程度參差的文辭表達，用心「看」到了學生服務的狀況，腦海中模擬當時的情景，既溫馨又感人，我開始微笑，試著去感受他們的成長與喜悅。縱然有些同學只有幾句話敷衍了事，但我相信他們內心的成長，應該比三言兩語要來得有深度些。這時我才驚醒，這些美好的人事物，一直在我身邊環繞，而我竟視為理所當然，心中只為那些不太美好的事情煩心。因為服務學習的經歷，一點一滴地流入學生的生命，看似無波，但多少也產生一些漣漪。 
          </p>
		  <p>&emsp;&emsp;轉眼課程實施將近五年，看到參與服務學習的同學發表自己從課程與服務的過程中，學習許多令人難忘的經驗與能力的培養。不論是輔導學童課業、照顧生病孩童、陪伴老人家、照顧聽障朋友、宣導資源回收、陪伴育幼院童、講故事給托兒所小朋友聽、協助民眾報稅、也有從討厭這門課的大一生，變成支持這門課的大二機構助理。許許多多服務的故事，讓人感到欣慰與感動，深深覺得每一位學生都是可造之材。 	       
		  </p>
		  <p>&emsp;&emsp;如果你問我對目前學生的看法，我開始欣賞這些年輕生命，體貼那怕受傷害、又猶疑不安的心，在他們輕狂外表的背後，更顯露對生命的渴慕的心、受教的心、及可能影響的深度。而我的眼光，也不再只停留在外表熱忱的展現或雋永的心得詞藻，而是細細欣賞每一個年輕生命所散發出來的光彩，及其生命的無限可能。服務學習就像一粒種子，深深埋入每個年輕的心田，相信有一天，這些種子會因環境適合而生根發芽與茁壯。     在靜宜，學生與老師們一起遇見了服務學習，這份機緣與歷程將會使我們的生命都得以學習與成長。
		  </p>
		  <p align="right">《原文摘自「在靜宜-遇見服務學習」，2006，靜宜大學。》</p>
