          <p style="text-align:right;font-size:14px;"><strong>師資培育中心專任教授  胡憶蓓</strong></font></p>
		  <p>&emsp;&emsp;相信大專校院裡絕大多數老師都非常敬業，期望傳授一身絕學；但往往無奈於學生興趣缺缺，他們不僅對課業如此態度，對人生似乎也茫茫然。老師們常深感無力與沮喪，不知到底要怎麼做，才能激起學生一點點的「感覺」與動力。回想以前的老教授不就在教室裡誨人不倦嗎？！為什麼現在的大學生都不像當年的自己認真地坐著乖乖學呢？
		  </p>　　 
		  <p>&emsp;&emsp;在推動服務學習的歷程中，發現其實有一些老師早已在他們的課程中落實服務學習理念。或許他們不知道服務學習這個名詞，但是他們的課程一向透過做中學，帶領學生走進社區，藉由實際應用，培養學生紮實的專業知能，並反省專業知識份子在社會中應扮演的角色與承擔的責任。
		  </p>
		  <p>&emsp;&emsp;自96學年度第2學期，服務學習發展中心即透過教學卓越計畫推動服務學習類型的專業課程，補助教學助理(TA)經費。原本的目的是期望在專業教師身上播灑服務學習理念的種子，但老實說，剛開始我是在發掘那些長期默默不斷付出、激發學生社會服務熱忱的教師，主動提供人力經費資源，希望藉此減輕他們的負擔。還記得當我向英文系周惠那老師(長期教導與訓練學生成為英文教學義工)提出TA人力時，她非常驚訝，沒想到資源居然會主動上門。其實，靜宜很幸運！有像周老師、林益仁老師、趙芝良老師等等，每每體會到他們對自身專業的熱忱，及將之傳承給年輕一代的執著，自己也不禁深受感動。 
          </p>
		  <p>&emsp;&emsp;服務學習，帶領學生走出傳統教室，讓學生知道世界之大，也意識到自己的存在與價值。專業服務歷程讓他們明瞭知識的用處、激發學習的動機與興趣、獲得成就與滿足、更意識到自己能力的不足與未來加強學習的方向。學生透過整個服務歷程，學習到的專業知識與人生功課，遠遠超過教科書記載的知識。 	       
		  </p>
		  <p>&emsp;&emsp; 「走出教室」，其實對老師來說也不是一件易事！交通、安全、經費、資源等都是客觀環境的阻礙因素，但是，老師的態度才是真正的關鍵！當我們期望培養積極解決問題的態度與能力、厚實學生適應變化萬千的社會能力、淬鍊吃苦耐勞、不畏艱難的態度等。若老師能親身實踐自己對大學生的期待與要求，那麼「親眼見到」的學生，或許就更能意識到自己未來發展的無限潛能。 
		  </p>
