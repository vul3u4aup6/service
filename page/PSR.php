          <p style="font-size:20px"><strong>實施辦法</strong></p><div class="clr"></div><p/>
		  <hr>
          <p style="text-align:center;font-size:20px"><strong>專業服務學習課程暨制度實施辦法</strong></font></p>
		  <p style="text-align:right">民國103年04月30日行政會議通過</p>
		  <p><span style="font-weight: bold">第一條</span>	為提升教學效果、增進學生專業知能，積極培養學生服務社會之熱忱與責任感，特訂定「靜宜大學專業服務學習實施辦法」（以下簡稱本辦法）。</p>
		  <p><span style="font-weight: bold">第二條</span>	專業服務學習，包含本校專任且具教師資格之教職員，於現行專業課程進行服務學習教學模式，以及本校教學或行政單位，成立自主性之志工服務團隊，納入服務學習精神，期能提供學生實務應用的機會，結合專業從事服務。</p>
		  <p><span style="font-weight: bold">第三條</span>	本辦法之服務對象，以需要專業服務協助的校外非營利組織或社區機構為主。服務內容為提供專業知能滿足合作機構的需求，服務方式如時間、工作安排、內容細節等，皆由授課教師或教學（行政）單位，與合作機構進行討論與規劃。</p>
		  <p><span style="font-weight: bold">第四條</span>	專業服務學習之實施期程為一學期，服務時間可包含學期期間及寒暑期。</p>
		  <p><span style="font-weight: bold">第五條</span>	專業服務學習之計畫，得申請活動補助經費（包含運費、保險費、印刷費、雜支）及兼任教學助理人事費，以協助服務活動之運作。</p>
		  <p><span style="font-weight: bold">第六條</span>	服務學習發展中心（以下簡稱本中心）於每學期開學後公開受理申請作業，有意申請之授課教師或教學（行政）單位，需於申請期限內提繳「專業服務學習計畫書」、「教學助理人事基本資料表」，由專業服務學習審核委員會進行審查。</p>
		  <p><span style="font-weight: bold">第七條</span>	專業服務學習審核委員會，由本中心主任邀請具專業服務學習理念與實務之教師數名組成，惟審查委員不得為當學期申請計畫之教師。</p>
		  <p><span style="font-weight: bold">第八條</span>	計畫申請通過之教師及教學（行政）單位，需參與專業服務學習教師團隊，並協助以下事項：</p>
		  <ol>
                  <li>1.需參與專業服務學習教師座談會、教學助理座談會、成果發表會。</li>
                  <li>2.協助規劃與執行專業服務學習活動，邀請合作機構填寫合作意向書。</li>
                  <li>3.提供專業服務學習推行成效，以供本中心日後推展之參考依據。</li>
                  <li>4.指導教學助理工作任務與相關職責，並協助完成經費核銷與成果報告。</li>
          </ol>
		  <p><span style="font-weight: bold">第九條</span>	本辦法經行政會議通過，校長公告後實施，修正時亦同。</p>
		  <p align="right">99.9.28 專業服務學習教師座談會修訂通過</p>
