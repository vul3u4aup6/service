		  <p style="font-size:16px"><strong>以&lceil;志工靜宜&rfloor;為核心價值的校園文化</strong></p>
          <p>&emsp;&emsp;靜宜大學由美國主教修女會(Sister of Providence)的修女創立，希望培養學生社會責任感，融合道德、品格、知識等各層面教育，展現天主教大學對社會的責任與對學生的教育使命。本校自民國89年開始推動<strong>&lceil;志工靜宜&rfloor;</strong>為核心價值的校園文化，積極辦理志工培訓，投入社區服務。在擴大服務範圍、回饋社區的理想下，逐步提升志願服務的質與量。因此本校於民國93學年度正式辦理<strong>&lceil;服務學習&rfloor;</strong> 課程，以有計畫性的課程內容以及多元社區服務型態，培養學生關懷社區公民責任、啟發服務利他的精神。</p>
		  
		  
		  <p style="font-size:16px"><strong>首創系統化教學發展之全校性服務學習課程</strong></p>
		  <p>1.&emsp;學生需完成12小時服務學習基礎講座、至少18小時社區服務，以及課堂討論與反思。</p>
		  <p>2.&emsp;全體大一學生，每周固定時段在教室內進行服務經驗之反省、分享與回饋的反思學習。</p>
		  <p>3.&emsp;透過有系統的課程規劃設計、督導、省思、評量與修正，提供學生參與社區服務機會，擴展學生生活經驗、深化生命內涵並培養關愛社會的人生觀。</p>
		  <p>4.&emsp;編制教師手冊等相關教學輔助教材，供每學期24班授課教師精進教學之用。並且辦理種子教師培訓課程，定期舉行月座談會議，分享、檢討與修正，協同提升教學品質。</p>
		  <p>5.&emsp;聘任本校具有社區服務經驗之高年級學生，擔任&lceil;課程助理&rfloor;，提供指導與經驗傳承。</p>
		  <p>6.&emsp;期末舉行服務學習成果展發表會，每年定期表揚服務優異之學生與助理。</p>
          
		   
		  <p style="font-size:16px"><strong>多元化社區服務型態</strong></p>
		  <p>&emsp;&emsp;&lceil;服務學習&rfloor 是一種經驗教育的模式。學校為拓展學習空間、開拓學習新視野並深化服務體驗，特別結合社區中各領域，包含&lceil;社會福利&rfloor;、&lceil;衛生醫療&rfloor;、&lceil;文化教育&rfloor;、&lceil;政府行政&rfloor;、&lceil;資訊服務&rfloor;以及&lceil;社區服務&rfloor;，藉由與社區多元良性互動，提供志願服務，培養關懷社區公民意識。每年度約投入2400位志願服務人力，每人至少提供18小時社區服務。</p>
		  
		  
		   
		  <p style="font-size:16px"><strong>全校性教學風氣之蔓延</strong></p>
		  <p>&emsp;&emsp;由靜宜大學所規劃的&lceil;服務學習&rfloor;課程，透過&lceil;志工靜宜&rfloor;校園核心價值，以有計畫性的課程內容及多元社區服務型態，培養學生關懷社區的公民責任、啟發服務他人的精神、提升學生生命教育之內涵與實現&lceil;志工靜宜&rfloor;之願景。&lceil;服務學習&rfloor;課程推展至今，已有超過70位專業教師參與課程帶領，與大台中地區80幾個非營利機構合作，結合校內外單位規劃多元化的服務內容，安排優質大學人力投入社區服務工作。校外服務層面含括老人、身心障礙者、兒童青少年以及社會上各弱勢族群。校內服務則包括宗輔室帶領學生辦理監獄讀書會及觀護所訪視、衛保組教導學生意外傷害簡易處置，門診及餐檢服務、尊生社帶領學生參與流浪動物保護及宣導、生態系所指導學生製作有機堆肥，設立生態溫室，並帶領學生參與生態保育宣導服務工作。此外，各科系老師更結合專業，帶領學生進入偏遠社區推展各項服務，如作文指導、成語應用、英文繪本閱讀、電腦軟體應用及生態科學等豐富的營隊活動，讓學生透過服務更加貼近社區的脈動，讓知識授與的場所打破學校教室的藩籬，真正讓學習落實於社區之中。
		  </p>　　 

		  
