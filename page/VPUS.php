          <p>&emsp;&emsp;靜宜大學自民國89年開始推動<strong>&lceil;志工靜宜&rfloor;</strong>，辦理許多志工培訓課程，期望能涵養本校學生具有服務的情操與社會的責任感，進而實踐<strong>&lceil;進德修業&rfloor;</strong> 的校訓，即是對天主教大學辦學理想的回應之一。</p>
          <p>&emsp;&emsp;然而每年僅培訓約100位志工，即使加上其他服務性社團及師資培育中心的50小時義工制度來投入社區服務與關懷弱勢族群，依舊不足。相較於全校一萬名學生之比例來看，<strong>&lceil;志工靜宜&rfloor;</strong> 的成效與理想仍有一段距離。至於過去行之有年的大一新生<strong>&lceil;人文素養與社會關懷&rfloor;</strong> 課程，期規劃為一學年，學生必須在上下學期進行打掃校園的<strong>&lceil;勞作教育&rfloor;</strong> 各一小時。亦即靜宜學生4年內義務性服務之機會，僅此2小時。除了量的不足之外，在質的部分，如課程之社會關懷部分，顯然也無法在有限的條件下進行計畫性地教導學習成效堪虞。</p>
          <p>&emsp;92年11月份學生事務處研擬推動<strong>&lceil;志工靜宜&rfloor;</strong> 服務學習方案&mdash;大一新生修習<strong>&lceil;服務學習&rfloor;</strong>課程之規劃，並經多次會議討論，最終在93年03月31日決議通過，將<strong>&lceil;服務學習&rfloor;</strong>課程規劃為大一新生單一學期零學分之校定共同必修課程。93年05月04日向董事會報告，獲得董事會一致的讚賞與認可。</p>
		  <p>&emsp;93年9月份全校正式開始實施<strong>&lceil;大一服務學習共同必修課程&rfloor;</strong>，期望藉此系統化、計畫性與整體性的規劃、督導、省思、評量與修正，協助<strong>&lceil;服務&mdash;學習&rfloor;</strong> 課程在靜宜大學之推動，使得全體靜宜人能夠養成志願服務的情操，提升學生生命教育之內涵，落實社會關懷與實現<strong>&lceil;志工靜宜&rfloor;</strong> 之願景。<p>
          <p>&emsp;94年8月份成立<strong>&lceil;服務學習發展中心&rfloor;</strong> 籌備辦公室，專職推動服務學習相關業務發展，試行2年後，評估組織功能與學生學習成效，正式決議納入教務處底下之二級單位。</p>
		  <p>&emsp;96年8月份正式成立<strong>&lceil;服務學習發展中心&rfloor;</strong>，除推動大一服務學習相關執行工作之外，更強化與課程之結合，積極推動成立不同議題之跨系院教師團隊藉由教學助理的加入與協助，共同合作規劃與推動專業服務學習之相關課程與方案</p>
		  
